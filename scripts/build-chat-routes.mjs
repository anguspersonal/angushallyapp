#!/usr/bin/env node
/**
 * build-chat-routes.mjs
 *
 * Crawls `src/app/**\/page.tsx`, derives the public route paths, applies
 * the deny list from `src/lib/chat/visibility.config.ts`, and writes
 * `src/lib/chat/tools.allowlist.generated.ts`.
 *
 * The output drives the `navigate` tool's `path.enum` (design §6) — so
 * the model literally cannot propose a navigation target that doesn't
 * exist or is on the deny list. Server-side re-check in the route
 * handler is defense-in-depth (FR-AGENT-2 / design §6.1).
 *
 * Route derivation rules:
 *   - `src/app/page.tsx`                  → `/`
 *   - `src/app/about/page.tsx`            → `/about`
 *   - `src/app/(group)/foo/page.tsx`      → `/foo`   (group segments don't appear in URL)
 *   - `src/app/users/[id]/page.tsx`       → dropped (dynamic routes are not navigable
 *                                                   by the model in v1 — task 4.1)
 *   - any route matching a `deny` pattern → dropped
 *
 * Dependency-free; runs from `prebuild` after build-chat-knowledge.mjs.
 *
 * See docs/chatbotv1/design.md §6.1 and docs/chatbotv1/tasks.md §4.1.
 */

import { readFileSync, readdirSync, writeFileSync, mkdirSync, statSync } from 'node:fs';
import { dirname, resolve, relative, sep } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const APP_DIR = resolve(REPO_ROOT, 'src/app');
const VISIBILITY_CONFIG_PATH = resolve(REPO_ROOT, 'src/lib/chat/visibility.config.ts');
const OUTPUT_PATH = resolve(REPO_ROOT, 'src/lib/chat/tools.allowlist.generated.ts');

/**
 * Recursively find every `page.tsx` under `src/app/`.
 */
function findPages(dir, out = []) {
  for (const entry of readdirSync(dir)) {
    const full = resolve(dir, entry);
    if (statSync(full).isDirectory()) {
      // Skip Next.js conventions that aren't route segments.
      if (entry.startsWith('_')) continue; // private folders
      findPages(full, out);
    } else if (entry === 'page.tsx' || entry === 'page.ts' || entry === 'page.jsx') {
      out.push(full);
    }
  }
  return out;
}

/**
 * Convert a `page.tsx` absolute path to a Next.js route string.
 * Returns `null` for dynamic routes (any segment in `[brackets]`).
 */
function pageToRoute(pagePath) {
  const rel = relative(APP_DIR, dirname(pagePath));
  if (!rel) return '/';
  const segments = rel.split(sep).filter((s) => {
    // Strip route-group `(group)` segments (Next.js convention — they
    // don't appear in the URL).
    if (s.startsWith('(') && s.endsWith(')')) return false;
    return true;
  });
  if (segments.some((s) => s.startsWith('[') && s.endsWith(']'))) {
    // Dynamic route — skip. Task 4.1: v1 doesn't navigate to dynamic routes.
    return null;
  }
  return '/' + segments.join('/');
}

/**
 * Load the visibility config and return its deny list.
 *
 * We parse the source rather than dynamic-importing because the .ts file
 * has TypeScript syntax. The structure is small + stable; a regex grab
 * is more honest than a fragile mini-evaluator. If the config grows
 * complex, swap this for a real `tsc --emit` step.
 */
function loadDenyList() {
  const src = readFileSync(VISIBILITY_CONFIG_PATH, 'utf8');
  // Match `deny: [ '/login', '/auth/**' ]` across newlines.
  const m = src.match(/deny:\s*\[([\s\S]*?)\]/);
  if (!m) {
    throw new Error('Could not find `deny` array in visibility.config.ts');
  }
  const items = m[1].match(/['"]([^'"]+)['"]/g) ?? [];
  return items.map((q) => q.slice(1, -1));
}

/**
 * Tiny glob-vs-path tester matching the matcher in src/lib/chat/visibility.ts:
 *   `*`  matches one segment (no `/`)
 *   `**` matches any chars (including `/`)
 *   trailing `/**` also matches the prefix alone.
 */
function matchesPattern(path, pattern) {
  const TRAIL = ' TS ';
  const DOUBLE = ' DS ';
  const SINGLE = ' SS ';
  const replaced = pattern
    .replace(/\/\*\*$/g, TRAIL)
    .replace(/\*\*/g, DOUBLE)
    .replace(/\*/g, SINGLE);
  const escaped = replaced.replace(/[.+?^${}()|[\]\\]/g, '\\$&');
  const expanded = escaped
    .replace(new RegExp(TRAIL, 'g'), '(?:/.*)?')
    .replace(new RegExp(DOUBLE, 'g'), '.*')
    .replace(new RegExp(SINGLE, 'g'), '[^/]*');
  return new RegExp(`^${expanded}$`).test(path);
}

function renderGeneratedFile(routes) {
  const banner = [
    '// AUTO-GENERATED by scripts/build-chat-routes.mjs — DO NOT EDIT.',
    '// Edit src/app pages or src/lib/chat/visibility.config.ts',
    '// and re-run `npm run build` (or `node scripts/build-chat-routes.mjs`).',
    '//',
    '// Committed so reviewers see content diffs in PRs.',
    '// See docs/chatbotv1/design.md §6.1.',
  ].join('\n');

  const literal = routes.map((r) => `  ${JSON.stringify(r)},`).join('\n');

  return [
    banner,
    '',
    'export const ROUTE_ALLOWLIST = [',
    literal,
    '] as const satisfies readonly string[];',
    '',
    'export type AllowedRoute = (typeof ROUTE_ALLOWLIST)[number];',
    '',
  ].join('\n');
}

function main() {
  const pages = findPages(APP_DIR);
  const routes = pages
    .map(pageToRoute)
    .filter((r) => r !== null)
    .sort((a, b) => a.localeCompare(b));

  const denyList = loadDenyList();
  const allowed = routes.filter((r) => !denyList.some((d) => matchesPattern(r, d)));
  const dropped = routes.length - allowed.length;

  // Deduplicate (defensive — Next.js shouldn't produce duplicates).
  const unique = Array.from(new Set(allowed));

  mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, renderGeneratedFile(unique), 'utf8');
  console.log(
    `[build-chat-routes] wrote ${unique.length} allowed routes (${dropped} dropped by deny-list, ${pages.length - routes.length} skipped as dynamic) → src/lib/chat/tools.allowlist.generated.ts`,
  );
}

main();
